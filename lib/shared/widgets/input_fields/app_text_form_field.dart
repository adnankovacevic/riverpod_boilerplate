        validator: validator,
        showCursor: showCursor,
        focusNode: focusNode,
        keyboardAppearance: keyboardAppearance,
        onChanged: onChanged,
        textAlignVertical: textAlignVertical,
        decoration: AppInputDecoration(
          suffixIconConstraints: suffixIconConstraints ??
              const BoxConstraints(minHeight: 24, minWidth: 24),
          isCollapsed: isCollapsed,
          isDense: isDense,
          suffixIcon: suffixIcon,
          prefixIcon: prefixIcon,
          context: context,
          contentPadding: contentPadding ?? const EdgeInsets.all(20.0),
          //labelText: labelText,
          //helperText: helperText,
          hintText: hintText,
          errorText: errorText,
        ),
        buildCounter: (BuildContext context,
                {required int currentLength,
                required bool isFocused,
                required int? maxLength}) =>
            null,
      ),
    ];

    double helperSpacing = 10;
    double labelSpacing = 10;

    if (helperText != null) {
      rows.insert(
        0,
        Padding(
          padding: EdgeInsets.only(bottom: helperSpacing),
          child: Text(
            helperText!,
            style: textTheme.bodyMedium,
            textAlign: TextAlign.start,
          ),
        ),
      );
      labelSpacing = 5;
    }

    if (labelText != null) {
      rows.insert(
        0,
        Padding(
          padding: EdgeInsets.only(bottom: labelSpacing),
          child: Text(
            labelText!,
            style: textTheme.bodyMedium?.w600,
            textAlign: TextAlign.start,
          ),
        ),
      );
    }

    return Column(
      crossAxisAlignment: CrossAxisAlignment.stretch,
      mainAxisAlignment: MainAxisAlignment.start,
      children: rows,
    );
  }
